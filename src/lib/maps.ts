export function getGymById(id: number): GymLocation | undefined {
  return gymLocations.find((gym) => gym.id === id);
}

export function getGymsByCity(city: string): GymLocation[] {
  return gymLocations.filter((gym) => gym.city.toLowerCase() === city.toLowerCase());
}

export function getGymsByRegion(region: string): GymLocation[] {
  return gymLocations.filter((gym) => gym.region.toLowerCase() === region.toLowerCase());
}

export function getUniqueRegions(): string[] {
  return [...new Set(gymLocations.map((gym) => gym.region))].sort();
}

export function getUniqueCities(): string[] {
  return [...new Set(gymLocations.map((gym) => gym.city))].sort();
}
